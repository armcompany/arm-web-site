let currentLang = 'pt';

function translateTextNodes(node, targetLang) {
  if (node.nodeType === Node.TEXT_NODE) {
    const text = node.nodeValue.trim();
    if (text) {
      if (!node.originalText) {
          node.originalText = text;
      }
      
      const currentVal = text;

      if (targetLang === 'en') {
        const enStr = dictEN[currentVal] || dictEN[node.originalText];
        if (enStr) {
          node.nodeValue = node.nodeValue.replace(currentVal, enStr);
        }
      } else if (targetLang === 'pt') {
        const ptStr = dictPT[currentVal] || dictPT[node.originalText] || node.originalText;
        if (ptStr) {
          node.nodeValue = node.nodeValue.replace(currentVal, ptStr);
        }
      }
    }
  } else if (node.nodeType === Node.ELEMENT_NODE) {
    if (node.tagName !== 'SCRIPT' && node.tagName !== 'STYLE') {
      Array.from(node.childNodes).forEach(child => translateTextNodes(child, targetLang));
    }
  }
}

function setLanguage(lang) {
  if (lang === currentLang) return;
  translateTextNodes(document.body, lang);
  currentLang = lang;
  
  // Update switcher styling
  document.querySelectorAll('.lang-switcher button').forEach(btn => {
      btn.style.opacity = '0.5';
  });
  const activeBtn = document.querySelector(`.lang-switcher button[onclick^="setLanguage('${lang}')"]`);
  if (activeBtn) activeBtn.style.opacity = '1';
}

document.addEventListener('DOMContentLoaded', () => {
    // Make sure 'pt' button is fully opaque at start
    document.querySelectorAll('.lang-switcher button').forEach(btn => {
        btn.style.opacity = '0.5';
    });
    const ptBtn = document.querySelector(`.lang-switcher button[onclick^="setLanguage('pt')"]`);
    if (ptBtn) ptBtn.style.opacity = '1';
    
    // We already start in the default language which is mostly PT and partially EN 
    // BUT we must fix the partially EN elements to PT if we want the original state to be pure PT, 
    // or we can just translate to 'pt' right at the beginning.
    translateTextNodes(document.body, 'pt');
});
